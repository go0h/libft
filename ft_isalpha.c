int			ft_isalpha(int c)
{
	unsigned char c_ch;

	c_ch = (unsigned char)c;
	if ((c_ch > 64 && c_ch < 91) || (c_ch > 96 && c_ch < 123))
		return (1);
	else
		return (0);
}
