int			ft_isalpha(char *str)
{
	int		i;
	int		flag;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] > 64 && str[i] < 91) || (str[i] > 96 && str[i] < 123))
			++i;
		else
			return (0);
	}
	return (1);
}
