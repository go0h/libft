int			ft_isprint(char *str)
{
	int		i;
	int		flag;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 31 && str[i] <= 127)
			++i;
		else
			return (0);
	}
	return (1);
}
