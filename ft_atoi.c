int					ft_atoi(const char *str)
{
	int		nb;
	int		flag;

	nb = 0;
	flag = 1;
	while ((*str > 8 && *str < 14) || *str == 32)
		++str;
	if (*str == '-' || *str == '+')
		flag = 44 - *str++;
	while (*str > 47 && *str < 58)
	{
		nb += *str++ - 48;
		if (*str > 47 && *str < 58)
			nb *= 10;
	}
	return (nb * flag);
}
